/* ── Pastel color values for PDF ── */
const PDF_COLORS = {
  mint: { r: 166, g: 217, b: 200 },
  peach: { r: 245, g: 200, b: 175 },
  lavender: { r: 200, g: 180, b: 220 },
  sky: { r: 175, g: 210, b: 240 },
  rose: { r: 235, g: 185, b: 200 },
  lemon: { r: 240, g: 225, b: 170 },
};

/* ── PDF Export Helper ── */
function exportToPdf(
  title: string,
  accentColor: keyof typeof PDF_COLORS,
  buildContent: (doc: jsPDF, startY: number) => void
) {
  const doc = new jsPDF({ unit: "mm", format: "a4" });
  const c = PDF_COLORS[accentColor];

  doc.setFillColor(c.r, c.g, c.b);
  doc.rect(0, 0, 210, 38, "F");

  doc.setFont("helvetica", "bold");
  doc.setFontSize(22);
  doc.setTextColor(50, 50, 50);
  doc.text(title, 20, 24);

  doc.setFont("helvetica", "normal");
  doc.setFontSize(9);
  doc.setTextColor(100, 100, 100);
  doc.text(new Date().toLocaleDateString("en-US", { weekday: "long", year: "numeric", month: "long", day: "numeric" }), 20, 32);

  doc.setDrawColor(c.r - 30, c.g - 30, c.b - 30);
  doc.setLineWidth(0.5);
  doc.line(20, 42, 190, 42);

  doc.setTextColor(40, 40, 40);
  buildContent(doc, 50);

  doc.setFontSize(8);
  doc.setTextColor(160, 160, 160);
  doc.text("Generated by NexusAI ✨", 20, 287);

  doc.save(`${title.replace(/\s+/g, "_").toLowerCase()}.pdf`);
}

